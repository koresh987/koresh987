    def change_settings(self, row: tk.Entry, column:tk.Entry, mines: tk.Entry):
        # —Å–¥–µ–ª–∞–µ–º –∏—Å–∫–ª—é—á–µ–Ω–∏–µ –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –≤–≤–æ–¥–∞ –¥—Ä—É–≥–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤ –≤–º–µ—Å—Ç–æ —Ü–∏—Ñ—Ä
        try:
            int(row.get()), int(column.get()), int(mines.get())
        except ValueError:
            showerror('–û—à–∏–±–∫–∞', '–í—ã –≤–≤–µ–ª–∏ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ') # –¥–ª—è –≤—ã–≤–æ–¥–∞ –æ–∫–Ω–∞ –æ—à–∏–±–∫–∏ –¥–æ–±–∞–≤–∏–º –º–æ–¥—É–ª—å  tkinter.messagebox  -> showerror
