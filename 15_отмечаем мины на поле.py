                btn = self.buttons[i][j]
                btn.number = count
                btn.grid(row=i, column=j, stick='NWES')  # –º–µ—Ç–æ–¥ stick='NWES' –ø–æ–∑–≤–æ–ª—è–µ—Ç —Ä–∞—Å—Ç—è–Ω—É—Ç—å –∫–Ω–æ–ø–∫–∏ –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ —Å—Ç–æ—Ä–æ–Ω–∞–º —Å–≤–µ—Ç–∞ –°–µ–≤–µ—Ä-–ó–∞–ø–∞–¥-–í–æ—Å—Ç–æ–∫-–Æ–≥
