        menubar.add_cascade(label='–§–∞–π–ª', menu=setting_menu) # –¥–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫—É –ì–ª–∞–≤–Ω–æ–≥–æ –º–µ–Ω—é



