        MineSweeper.ROW = int(row.get())               # –ø–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ —Å –æ–∫–Ω–∞ –Ω–∞—Å—Ç—Ä–æ–µ–∫ –ø–µ—Ä–µ–≤–æ–¥—è –≤ –∏–Ω—Ç –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ–º –≤ –Ω–∞—à—É –≥–ª–æ–±–∞–ª—å–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
        MineSweeper.COLUMNS = int(column.get())
        MineSweeper.MINES = int(mines.get())

        '''–ø–æ—Å–ª–µ –Ω–∞—Å—Ç—Ä–æ–µ–∫ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞–µ–º –∏–≥—Ä—É'''
        self.reload()


